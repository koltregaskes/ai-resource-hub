const VALID_DATE_PRECISIONS = new Set<AiMilestoneDatePrecision>(['day', 'month', 'year']);
const VALID_CATEGORIES = new Set<AiMilestoneCategory>([
  'model_release',
  'lab_founded',
  'research_breakthrough',
  'product_launch',
  'benchmark_milestone',
  'industry_event',
  'funding',
  'acquisition',
  'policy',
]);
const VALID_KINDS = new Set<AiMilestoneKind>(['launch', 'founded', 'milestone', 'anniversary']);
const VALID_SIGNIFICANCE = new Set<AiMilestoneSignificance>(['major', 'notable', 'reference']);
const VALID_STATUS = new Set<AiMilestoneStatus>(['verified', 'tracking', 'needs_review']);
const VALID_CONFIDENCE = new Set<AiMilestoneConfidence>(['high', 'medium', 'low']);
const VALID_SOURCE_TYPES = new Set<AiMilestoneSourceType>([
  'official_news',
  'official_docs',
  'official_research',
  'official_company',
  'official_registry',
  'secondary_report',
]);

function isIsoDate(value: string): boolean {
  return /^\d{4}-\d{2}-\d{2}$/.test(value);
}

export function validateAiMilestoneSeed(seed: AiMilestoneSeed[] = aiMilestoneSeed): AiMilestoneValidationSummary {
  const ids = new Set<string>();
  const slugs = new Set<string>();

  for (const milestone of seed) {
    if (ids.has(milestone.id)) throw new Error(`Duplicate milestone id: ${milestone.id}`);
    if (slugs.has(milestone.slug)) throw new Error(`Duplicate milestone slug: ${milestone.slug}`);
    ids.add(milestone.id);
    slugs.add(milestone.slug);

    if (!VALID_DATE_PRECISIONS.has(milestone.datePrecision)) throw new Error(`Invalid date precision for ${milestone.id}`);
    if (!VALID_CATEGORIES.has(milestone.category)) throw new Error(`Invalid category for ${milestone.id}`);
    if (!VALID_KINDS.has(milestone.kind)) throw new Error(`Invalid kind for ${milestone.id}`);
    if (!VALID_SIGNIFICANCE.has(milestone.significance)) throw new Error(`Invalid significance for ${milestone.id}`);
    if (!VALID_STATUS.has(milestone.status)) throw new Error(`Invalid status for ${milestone.id}`);
    if (!VALID_CONFIDENCE.has(milestone.confidence)) throw new Error(`Invalid confidence for ${milestone.id}`);
    if (milestone.datePrecision === 'day' && (!milestone.month || !milestone.day)) throw new Error(`Day precision requires month and day for ${milestone.id}`);
    if (milestone.datePrecision === 'month' && !milestone.month) throw new Error(`Month precision requires month for ${milestone.id}`);
    if (milestone.datePrecision !== 'day' && milestone.exactAnniversaryEligible) throw new Error(`Only day-precision milestones can be exact anniversaries: ${milestone.id}`);
    if (milestone.trackingDate && !isIsoDate(milestone.trackingDate.date)) throw new Error(`Tracking date must be ISO YYYY-MM-DD for ${milestone.id}`);
    if (milestone.sources.length === 0) throw new Error(`Milestone ${milestone.id} has no sources`);
    if (!milestone.sources.some((source) => source.role === 'canonical')) throw new Error(`Milestone ${milestone.id} needs at least one canonical source`);

    for (const source of milestone.sources) {
      if (!VALID_SOURCE_TYPES.has(source.sourceType)) throw new Error(`Invalid source type on ${milestone.id}: ${source.sourceType}`);
      if (!source.url.startsWith('https://')) throw new Error(`Source URLs must be https for ${milestone.id}: ${source.url}`);
    }
  }

  return {
    milestones: seed.length,
    verified: seed.filter((item) => item.status === 'verified').length,
    tracking: seed.filter((item) => item.status === 'tracking').length,
    needsReview: seed.filter((item) => item.status === 'needs_review').length,
    exactAnniversaries: seed.filter((item) => item.exactAnniversaryEligible).length,
  };
}
